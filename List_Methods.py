friends=[1,2,4,2,5,3]
print(friends)

friends.append(6) # append->add at end 
print(friends)

friends.sort()
print(friends)

friends.reverse()
print(friends)

friends.insert(3,7) #this will add 7 at index 3 
print(friends)

friends.pop(2) #will delete the element at index 2 and return its value 
print(friends)

friends.remove(5) #will remove 5 from the list 
print(friends)



